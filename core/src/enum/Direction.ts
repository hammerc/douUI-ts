namespace douUI {
    /**
     * 控件方向枚举
       * @author wizardc
     */
    export const enum Direction {
        /**
         * 水平从左到右增长
         */
        ltr,
        /**
         * 水平从右到左增长
         */
        rtl,
        /**
         * 竖直从上到下增长
         */
        ttb,
        /**
         * 竖直从下到上增长
         */
        btt
    }
}
