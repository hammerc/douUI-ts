namespace douUI {
    /**
     * 列对其枚举
     * @author wizardc
     */
    export const enum ColumnAlign {
        /**
         * 不将行两端对齐
         */
        left,
        /**
         * 通过增大水平间隙将行两端对齐
         */
        justifyUsingGap,
        /**
         * 通过增大行高度将行两端对齐
         */
        justifyUsingWidth
    }
}
